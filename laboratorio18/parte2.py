#Desarrollo de operaciones matematicas sin funciones
numero_1 = int(input("Ingrese el primer numero entero: "))
numero_2 = int(input("Ingrese el segundo numero entero: "))

print(f' -la suma de {numero_1} y {numero_2} es {numero_1+numero_2}')
print(f' -la resta de {numero_1} y {numero_2} es {numero_1 - numero_2}')
print(f' -la multiplicacion de {numero_1} y {numero_2} es {numero_1*numero_2}')
print(f' -la division de {numero_1} y {numero_2} es {numero_1/numero_2}')